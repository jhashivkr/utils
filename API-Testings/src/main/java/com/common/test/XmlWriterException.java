package com.common.test;


public class XmlWriterException extends Exception
{

	/**
	 * 
	 */
	private static final long serialVersionUID = -7803302489564319871L;

	public XmlWriterException()
	{
		super();
	}
	
	public XmlWriterException(String str)
	{
		super(str);
	}
	
	public XmlWriterException(Throwable t)
	{
		super(t);
	}
	
}
